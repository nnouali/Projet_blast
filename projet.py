def calculate_alignment_score(seq1, seq2, substitution_matrix, gap_penalty=-1):
    if len(seq1) != len(seq2):
        raise ValueError("Les séquences doivent avoir la même longueur")

    score = 0
    for char1, char2 in zip(seq1, seq2):
        if char1 == '-' or char2 == '-':
            score += gap_penalty
        else:
            score += substitution_matrix[char1][char2]

    return score

def find_combinations_above_threshold(sequence, threshold, substitution_matrix, gap_penalty=-1):
    amino_acids = list(substitution_matrix.keys())
    triplets = [sequence[i:i + 3] for i in range(0, len(sequence))]
    combinations = []

    for triplet in triplets:
        print(triplet)
        if len(triplet) == 3:
            for phase in range(1, 4):
                print("LAAAAAAA")
                phase_triplet = triplet[phase - 1::3]
                if len(phase_triplet) == 3:
                    for combo in product(amino_acids, repeat=3):
                        print("ICIIIIIIIIIIII")
                        combo_str = ''.join(combo)
                        score = calculate_alignment_score(combo_str, phase_triplet, substitution_matrix, gap_penalty)
                        if score >= threshold:
                            combinations.append((combo_str, phase_triplet, score))

    return combinations

